package com.company;

import java.util.ArrayList;

public class Players {
    private String name = "";

    private int strength = 0;
    private int health = 0;
    private int stamina = 0;
    private int speed = 0;
    private int attackPower = 0;

    private boolean running = false;
    private boolean arrested = false;

    private ArrayList<String> abilities = new ArrayList<String>();

    public Players() {
        ArrayList<String> defaultAttack= new ArrayList<String>();
        defaultAttack.add("Attack another character");
        this.setAbilities(defaultAttack);
    }

    public ArrayList<String> getAbilities() {
        return abilities;
    }

    public void setAbilities(ArrayList<String> abilities) {
        this.abilities = abilities;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getStrength() {
        return strength;
    }

    public void setStrength(int strength) {
        this.strength = strength;
    }

    public int getHealth() {
        return health;
    }

    public void setHealth(int health) {
        this.health = health;
    }

    public int getStamina() {
        return stamina;
    }

    public void setStamina(int stamina) {
        this.stamina = stamina;
    }

    public int getSpeed() {
        return speed;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }

    public int getAttackPower() {
        return attackPower;
    }

    public void setAttackPower(int attackPower) {
        this.attackPower = attackPower;
    }

    public boolean isRunning() {
        return running;
    }

    public void setRunning(boolean running) {
        this.running = running;
    }

    public boolean isArrested() {
        return arrested;
    }

    public void setArrested(boolean arrested) {
        this.arrested = arrested;
    }
}
